#Retrasar un vuelo:
UPDATE air_control_vmgr.vuelo SET
	#He buscado por internet la manera de sumar un intervalo de texto a DATETIME, esto se realiza, definiendo un INTERVAL de tiempo a sumar
	#De no hacerlo así, tendríamos que asignar fecha_salida = '2024-11-23 17:00:00', fecha_llegada = '2024-11-23 18:00:00'
	fecha_salida = fecha_salida + INTERVAL 45 MINUTE,
	fecha_llegada = fecha_llegada + INTERVAL 45 MINUTE
WHERE 
	id_vuelo = 14;

#Cambiar el piloto en otro vuelo:
UPDATE
	#Solamente vamos a cambiar un campo en tripulante_vuelo, pero necesitamos saber qué tripulante de esa id_vuelo és el piloto
	#Para realizar esto, haremos un join con la tabla de tripulantes
	(air_control_vmgr.tripulante_vuelo AS tv
JOIN
	air_control_vmgr.tripulante AS t
ON 
	tv.doc_id_tripulante = t.doc_id_tripulante)
SET
	tv.doc_id_tripulante = '14397596P' #Id del nuevo piloto
WHERE
	t.doc_id_tripulante = t.doc_id_capitan #El tripulante con la misma id que el capitán, será el piloto/capitán
AND
	tv.id_vuelo = 11; #Vuelo en el que queremos cambiar el piloto


